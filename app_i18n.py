APP_STRINGS = {"en": EN, **_DRAFTS}


def detect_system_language() -> str:
    raw = (
        locale.getlocale()[0]
        or locale.getdefaultlocale()[0]
        or os.environ.get("LANG", "")
        or ""
    )
    normalized = raw.replace("_", "-").lower()
    if normalized.startswith("pt"):
        return "pt-BR"
    if normalized.startswith("zh-hant") or normalized.startswith("zh-tw") or normalized.startswith("zh-hk"):
        return "zh-TW"
    if normalized.startswith("zh"):
        return "zh-CN"
    for code, _name in SUPPORTED_LANGUAGES:
        if code != "system" and normalized.startswith(code.lower()):
            return code
    return "en"


def resolve_language(code: str | None) -> str:
    if code == "system":
        return detect_system_language()
    supported = {language for language, _name in SUPPORTED_LANGUAGES}
    return code if code in supported and code != "system" else "en"


def language_label(code: str, active_language: str = "en") -> str:
    if code == "system":
        return translate(active_language, "language.system")
    for language, label in SUPPORTED_LANGUAGES:
        if language == code:
            return label
    return code


def translate(language: str | None, key: str, **values: object) -> str:
    resolved = resolve_language(language)
    text = APP_STRINGS.get(resolved, {}).get(key) or EN.get(key) or key
    if values:
        try:
            return text.format(**values)
        except (KeyError, ValueError):
            return text
    return text
